package com.pyramid;
import java.util.*;

public class Main {

    public static void main(String[] args) {
	// write your code here
        for(int i=1;i<=5;i++){
            for(int j = 4;j>=0;j--){
                System.out.print(" ");
            }}

        for(int k = 1;k<=5;k++){
                System.out.print("x");
            }
            System.out.print("\n");
        }

}
